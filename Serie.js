"use strict";
class Serie {
    constructor(id, nombre, plataforma, temporadas, sinopsis, link, imagen) {
        this.id = id;
        this.nombre = nombre;
        this.plataforma = plataforma;
        this.temporadas = temporadas;
        this.sinopsis = sinopsis;
        this.link = link;
        this.imagen = imagen;
    }
}
